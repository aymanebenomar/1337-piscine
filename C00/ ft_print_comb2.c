#include <unistd.h>

void ft_print_comb2(void){
	int		i;
	int		x;
	char 	s[5];

	i = 0;
	while (i <= 98)
	{
		x = i + 1;
		while (x <= 99)
		{
			s[0] = '0' + (i / 10);
			s[1] = '0' + (i % 10);
			s[2] = ' ';
			s[3] = '0' + (x / 10);
			s[4] = '0' + (x % 10);
			write (1, s, 5);
			if (!(i == 98 && x == 99))
				write (1, ", ", 2);
			x++;
		}
		i++;
	}
}

int main(){
	ft_print_comb2();
	return 0;
}